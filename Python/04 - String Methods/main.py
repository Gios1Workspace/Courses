# Now, since you know more about strings: In python there are string methods, that let us modify the strings itself, 
# obviously. There are different build-in string methods that you can use:

some_string = 'Hello World!'
value = 0

# METHOD                    # WHAT IT DOES/DESCRIPTION            
#-------------------------------------------------------------------------------------------------------------------                 
some_string.capitalize()    # This method capitalizes every letter in the given string 
#-------------------------------------------------------------------------------------------------------------------
some_string.casefold()      # This method lowers every letter in the given string
#-------------------------------------------------------------------------------------------------------------------
some_string.center()        # This method returns a centered string in the terminal
#-------------------------------------------------------------------------------------------------------------------
some_string.count()         # This method returns how many times a letter is occured and repeated in a string
#-------------------------------------------------------------------------------------------------------------------
some_string.encode()        # This method returns the encoded version of the given string
#-------------------------------------------------------------------------------------------------------------------
some_string.endswith()      # Comprasion method, returns true if the string ends with a specified value
#-------------------------------------------------------------------------------------------------------------------
some_string.expandtabs()    # Expands the tab of the string
#-------------------------------------------------------------------------------------------------------------------
some_string.find(value)     # Finds the specified value in the string that it contains method(--value--)
#-------------------------------------------------------------------------------------------------------------------
some_string.format()        # Formats and returns the specified values into a string
